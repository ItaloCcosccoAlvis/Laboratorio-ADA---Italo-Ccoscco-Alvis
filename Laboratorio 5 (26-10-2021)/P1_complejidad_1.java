public class P1_complejidad_1{
    public static void main(String[] args) {
        
        // Problema 1: ¿Cúal es la complejidad?
        int n = 10;
        // Bucle ascendente
        for(int i = 0; i < n; i++){
            System.out.println("statement");
        }
        // Complejidad: O(n)
    }
}